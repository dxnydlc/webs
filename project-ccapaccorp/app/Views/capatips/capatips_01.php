                        <p class="course-details__overview__text" style="font-size:22px;margin-bottom: 3px;text-align: justify;" >
                        En atención a la Ley N° 27735, todos los trabajadores sujetos al régimen laboral de la actividad privada tienen derecho a percibir 2 gratificaciones en el año, una en el mes de julio con ocasión de fiestas patrias y la otra en el mes de diciembre con motivo de navidad.
                        <br/><br/>
                        Cabe precisar que, para tener derecho a la gratificación de fiestas patrias, es requisito que el trabajador se encuentre laborando en la oportunidad en que corresponda percibir el beneficio o estar en uso del descanso vacacional, de licencia con goce de remuneraciones percibiendo subsidios de la seguridad social o por accidentes de trabajo. Asimismo, el tiempo de servicios para efectos del cálculo se determina por cada mes calendario completo laborado en el período correspondiente. Los días que no se consideren tiempo efectivamente laborado se deducirán a razón de un treintavo de la fracción correspondiente
                        <br/><br/>
                        Ahora bien, el monto de cada una de las gratificaciones es equivalente a la remuneración que perciba el trabajador en la oportunidad en que corresponde otorgar el beneficio. En ese sentido, para la gratificación de fiestas patrias se considera la remuneración que perciba el trabajador en el mes de julio. Para este efecto, se considera como remuneración, a la remuneración básica y a todas las cantidades que regularmente perciba el trabajador en dinero o en especie como contraprestación de su labor, cualquiera sea su origen o la denominación que se les dé, siempre que sean de su libre disposición.
                        <br/><br/>
                        La gratificación de fiestas patrias, deberá ser abonada en la primera quincena del mes de julio. Esto es, hasta el lunes 15 de julio 2024.
                        <br><br/>
                        En el caso, que el trabajador cuente con menos de 6 mes, percibirá la gratificación en forma proporcional a los meses laborados.
                        <br/><br/>
                        Pero sin duda lo más esperado por los trabajadores es que la gratificación de julio no se encuentra afecta a aportaciones, contribuciones ni descuentos de alguna índole; excepto aquellos otros descuentos establecidos por ley o autorizados por el trabajador. Es decir, el trabajador recibirá su gratificación más el 9% de EsSalud o el 6.75% en el caso que la empresa cuente con el beneficio de EPS (Empresa Prestadora de Salud) y no estará afecto a ningún descuento. Esto es, que el monto que abonan los empleadores por concepto de aportaciones al Seguro Social de Salud – EsSalud con relación a las gratificaciones de julio y diciembre son abonados a los trabajadores bajo la modalidad de bonificación extraordinaria.
                        <br/><br/>
                        <strong>Ejemplo 1</strong>
                        
                        <br/><br/>
                        Si un trabajador labora desde el 01 de enero de 2024, gana una remuneración de S/ 2,200 y se encuentra afiliado a EsSalud, percibirá como gratificación por fiestas patrias S/ 2,398 sin algún descuento de por medio.
                        <br><br>
                        S/ 2,200 + 9% EsSalud = S/ 2,398
                        <br><br>
                        <strong>Ejemplo 2</strong>
                        <br><br/>
